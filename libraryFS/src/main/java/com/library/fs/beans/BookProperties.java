package com.library.fs.beans;

public class BookProperties {

	private String book_title = null;
	private String[] book_authors = null;
	private String book_ISBN = null;
	private String book_edition = null;
	private String book_released_date = null;
	private String uploaded_by = null;
	private String school_name = null;
	private String date = null;
	private String book_genre = null;

	public String getBook_title() {
		return book_title;
	}

	public void setBook_title(String book_title) {
		this.book_title = book_title;
	}

	public String[] getBook_authors() {
		return book_authors;
	}

	public void setBook_authors(String[] book_authors) {
		this.book_authors = book_authors;
	}

	public String getBook_ISBN() {
		return book_ISBN;
	}

	public void setBook_ISBN(String book_ISBN) {
		this.book_ISBN = book_ISBN;
	}

	public String getBook_edition() {
		return book_edition;
	}

	public void setBook_edition(String book_edition) {
		this.book_edition = book_edition;
	}

	public String getBook_released_date() {
		return book_released_date;
	}

	public void setBook_released_date(String book_released_date) {
		this.book_released_date = book_released_date;
	}

	public String getUploaded_by() {
		return uploaded_by;
	}

	public void setUploaded_by(String uploaded_by) {
		this.uploaded_by = uploaded_by;
	}

	public String getSchool_name() {
		return school_name;
	}

	public void setSchool_name(String school_name) {
		this.school_name = school_name;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getBook_genre() {
		return book_genre;
	}

	public void setBook_genre(String book_genre) {
		this.book_genre = book_genre;
	}

}
